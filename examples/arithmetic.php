<?php
// Arithmetic operations example
$x = 15;
$y = 3;

echo "=== Arithmetic Demo ===\n";
echo "x = " . $x . ", y = " . $y . "\n";

echo "Addition: " . $x . " + " . $y . " = " . ($x + $y) . "\n";
echo "Subtraction: " . $x . " - " . $y . " = " . ($x - $y) . "\n";
echo "Multiplication: " . $x . " * " . $y . " = " . ($x * $y) . "\n";
echo "Division: " . $x . " / " . $y . " = " . ($x / $y) . "\n";